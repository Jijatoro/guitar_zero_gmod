local PANEL = {}
local function clr() return jlib.cfg.themes[jlib.cfg.theme]  or {} end
local data_font = {
    ["main"] = {
        txt = "s1-24"
    },
    ["anime"] = {
        txt = "a3-24"
    },
    ["fantasy"] = {
        txt = "f1-24"
    },
    ["cyber"] = {
        txt = "c2-24"
    },    
    ["horror"] = {
        txt = "h4-24"
    },
    ["terminal"] = {
        txt = "s1-24"
    } 
}

--------------------------------------------------------------------------------------------------------------|>
--[+] Main functions :--:--:--:--:--:--:--:--:--:--:--:}>                                                     |>
--------------------------------------------------------------------------------------------------------------|>
function PANEL:Init()
    self:SetFont(jlib.vgui.GetFont(data_font, "txt"))
    self:SetTextColor(clr()["t_p1"])
end

vgui.Register("jlib.label-main", PANEL, "DLabel")

